package com.example.pesterminatr;

public class bookingDetailsList {
	
	String name,problem,address,date,time,price,rstatus,id,restid;

	public bookingDetailsList(String name, String problem, String address,
			String date, String time, String price, String rstatus, String id,
			String restid) {
		super();
		this.name = name;
		this.problem = problem;
		this.address = address;
		this.date = date;
		this.time = time;
		this.price = price;
		this.rstatus = rstatus;
		this.id = id;
		this.restid = restid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getProblem() {
		return problem;
	}

	public void setProblem(String problem) {
		this.problem = problem;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getRstatus() {
		return rstatus;
	}

	public void setRstatus(String rstatus) {
		this.rstatus = rstatus;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getRestid() {
		return restid;
	}

	public void setRestid(String restid) {
		this.restid = restid;
	}

	

	

	
	
	
	
	

}
